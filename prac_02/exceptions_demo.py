try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    while denominator == 0:
        print("Denominator cannot be zero!")
        denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
except ZeroDivisionError:
    print("Cannot divide by zero!")
print("Finished.")

"""
A ValueError occur when you're trying to assign the value to an unsupported type of object
A ZeroDivisionError when you're trying to divide by zero
To avoid ZeroDivisionError, do a while loop for the denominator input, keep asking the user to type the 
denominator again until it is not zero
"""